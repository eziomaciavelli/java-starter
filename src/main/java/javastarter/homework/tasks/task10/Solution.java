package javastarter.homework.tasks.task10;

public class Solution {

    public static void foo(int num, char ch, String s) {

        for (int i = 0; i <= num; i++) {
            if (s.equalsIgnoreCase(String.valueOf(false))) {
                System.out.print(ch);
            } else {
                System.out.println(ch);
            }
        }
    }
}

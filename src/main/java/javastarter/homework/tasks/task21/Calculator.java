package javastarter.homework.tasks.task21;

public class Calculator {

    public static double mul(double firstNumber, double lastNumber) {
        return firstNumber * lastNumber;
    }

    public static double div(double firstNumber, double lastNumber) {
        return firstNumber / lastNumber;
    }

    public static double add(double firstNumber, double lastNumber) {
        return firstNumber + lastNumber;
    }

    public static double sub(double firstNumber, double lastNumber) {
        return firstNumber - lastNumber;
    }
}
